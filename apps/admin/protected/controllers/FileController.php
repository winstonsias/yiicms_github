class FileController extends BackendBaseController
{
 /* 文件上传 */
    public function actionUpload(){
		$return  = array('status' => 1, 'info' => '上传成功', 'data' => '');
		/* 调用文件上传组件上传文件 */
		$File = new File();
		$file_driver = C('DOWNLOAD_UPLOAD_DRIVER');
		$info = $File->upload(
			$_FILES,
			C('DOWNLOAD_UPLOAD'),
			C('DOWNLOAD_UPLOAD_DRIVER'),
			C("UPLOAD_{$file_driver}_CONFIG")
		);

        /* 记录附件信息 */
        if($info){
            $return['data'] = think_encrypt(json_encode($info['download']));
            $return['info'] = $info['download']['name'];
        } else {
            $return['status'] = 0;
            $return['info']   = $File->getMyError();
        }

        /* 返回JSON数据 */
        exit(CJSON::encode($return));
    }

    /**
     * 上传图片
     * @author huajie <banhuajie@163.com>
     */
    public function actionUploadPicture(){
        //TODO: 用户登录检测

        /* 返回标准数据 */
        $return  = array('status' => 1, 'info' => '上传成功', 'data' => '');

        /* 调用文件上传组件上传文件 */
        $Picture = new Picture();
        $pic_driver = strtoupper(C('PICTURE_UPLOAD_DRIVER'));
        $info = $Picture->upload(
            $_FILES,
            C('PICTURE_UPLOAD'),
            C('PICTURE_UPLOAD_DRIVER'),
            C("UPLOAD_{$pic_driver}_CONFIG")
        ); //TODO:上传到远程服务器

        /* 记录图片信息 */
        if($info){
            $return['status'] = 1;
            $return = array_merge($info['download'], $return);
            $this->ajaxReturn($return['info'],'',$return['status'],$info['download']);
        } else {
            $return['status'] = 0;
            $return['info']   = $Picture->getMyError();
            $this->ajaxReturn($return['info'],'',$return['status']);
        }
       
        
         
    }
}